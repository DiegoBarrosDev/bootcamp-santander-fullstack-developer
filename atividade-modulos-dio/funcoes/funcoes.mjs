const mostraIdade = function (nome, idade) {
    return `A idade de ${nome} é ${idade}!`
}

const mostraCidade = function (nome, cidade) {
    return `A cidade de ${nome} é ${cidade}!`
}

const mostraHobby = function (nome, hobby) {
    return `O hobby de ${nome} é ${hobby}!`
}

export {
    mostraIdade,
    mostraCidade,
    mostraHobby
}